export const getCities = (): City[] => {
  return cities.sort((a, b) => a.name.localeCompare(b.name));
};

export const getCityByName = (name: string): City | undefined => {
  return cities.find((city) => city.name.toLowerCase() === name.toLowerCase());
};

export const getBarangaysByCity = (cityName: string): { name: string; code?: string }[] => {
  const city = getCityByName(cityName);
  return city ? city.barangays : [];
};

export const getCitiesByProvince = (provinceName: string): City[] => {
  return cities
    .filter((city) => city.province.toLowerCase() === provinceName.toLowerCase())
    .sort((a, b) => a.name.localeCompare(b.name));
};

export const getCitiesByRegion = (regionCode: string): City[] => {
  return cities
    .filter((city) => city.region.toLowerCase() === regionCode.toLowerCase())
    .sort((a, b) => a.name.localeCompare(b.name));
};

export const searchCities = (query: string): City[] => {
  const lowerQuery = query.toLowerCase();
  return cities
    .filter((city) => city.name.toLowerCase().includes(lowerQuery))
    .sort((a, b) => a.name.localeCompare(b.name));
};

export const searchBarangays = (cityName: string, query: string): { name: string; code?: string }[] => {
  const barangays = getBarangaysByCity(cityName);
  const lowerQuery = query.toLowerCase();
  return barangays.filter((brgy) => brgy.name.toLowerCase().includes(lowerQuery));
};
